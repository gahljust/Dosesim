/// \file dosesimActionInitialization.cc
/// \brief Implementation of the dosesimActionInitialization class

#include "dosesimActionInitialization.hh"
#include "dosesimPrimaryGeneratorAction.hh"
#include "dosesimRunAction.hh"
#include "dosesimEventAction.hh"
#include "dosesimSteppingAction.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

dosesimActionInitialization::dosesimActionInitialization()
 : G4VUserActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

dosesimActionInitialization::~dosesimActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void dosesimActionInitialization::BuildForMaster() const
{
  dosesimRunAction* runAction = new dosesimRunAction;
  SetUserAction(runAction);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void dosesimActionInitialization::Build() const
{
  SetUserAction(new dosesimPrimaryGeneratorAction);

  dosesimRunAction* runAction = new dosesimRunAction;
  SetUserAction(runAction);
  
  dosesimEventAction* eventAction = new dosesimEventAction(runAction);
  SetUserAction(eventAction);
  
  SetUserAction(new dosesimSteppingAction(eventAction));
}  

